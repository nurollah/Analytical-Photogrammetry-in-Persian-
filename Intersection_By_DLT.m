% Bismillah
function [XYZ]=Intersection_By_DLT(xy1, xy2, DLT1, DLT2)
%
x1 = xy1(1);
y1 = xy1(2);
x2 = xy2(1);
y2 = xy2(2);
%
L1_1 = DLT1(1,1);
L2_1 = DLT1(1,2);
L3_1 = DLT1(1,3);
L4_1 = DLT1(1,4);
L5_1 = DLT1(2,1);
L6_1 = DLT1(2,2);
L7_1 = DLT1(2,3);
L8_1 = DLT1(2,4);
L9_1 = DLT1(3,1);
L10_1 = DLT1(3,2);
L11_1 = DLT1(3,3);
%
L1_2 = DLT2(1,1);
L2_2 = DLT2(1,2);
L3_2 = DLT2(1,3);
L4_2 = DLT2(1,4);
L5_2 = DLT2(2,1);
L6_2 = DLT2(2,2);
L7_2 = DLT2(2,3);
L8_2 = DLT2(2,4);
L9_2 = DLT2(3,1);
L10_2 = DLT2(3,2);
L11_2 = DLT2(3,3);
%
A = [L1_1 - L9_1*x1, L2_1 - L10_1*x1, L3_1 - L11_1*x1;...
     L5_1 - L9_1*y1, L6_1 - L10_1*y1, L7_1 - L11_1*y1;...
     L1_2 - L9_2*x2, L2_2 - L10_2*x2, L3_2 - L11_2*x2;...
     L5_2 - L9_2*y2, L6_2 - L10_2*y2, L7_2 - L11_2*y2];
 L = [x1-L4_1;...
      y1-L8_1;...
      x2-L4_2;...
      y2-L8_2];
  XYZ=inv(A'*A)*A'*L;
  